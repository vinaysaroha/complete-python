# string are sequence of character using the syntax of either single quote or double quotes
print('hello')
print("hello")
print("hello I didn't get that")
# string are order sequence it means that we can using indexing and slicing to grab sub-sections of the string
# character:            h    e    l    l    o
# index:                0    1    2    3    4
# reverse index:        0    -4    -3  -2   -1

# Slicing allow you to grab a subsection of multiple characters a slice of the string
# this has the following syntax : [start:stop:steps]
print('hello world how are you \nI am good') # \n
print('hello world how are you \tI am good')
print('hello')
print(len('hello'))